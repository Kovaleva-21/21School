#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*ptr;
	unsigned char	sym;
	size_t			i;

	i = 0;
	ptr = (unsigned char *)s;
	sym = c;
	while (n > 0)
	{
		if (ptr[i] == sym)
			return (&ptr[i]);
		i++;
		n--;
	}
	return (NULL);
}
