#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char	*ptr;
	size_t			i;
	unsigned char	sym;

	sym = (unsigned char)c;
	ptr = (unsigned char *)b;
	i = 0;
	while (len > i)
	{
		ptr[i] = sym;
		i++;
	}
	return (ptr);
}
