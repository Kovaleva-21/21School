#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char			*ptr;
	unsigned char	sym;
	size_t			i;

	ptr = (char *)s;
	sym = c;
	i = 0;
	while (ptr[i] != '\0')
	{
		if (ptr[i] == sym)
			return (&ptr[i]);
		i++;
	}
	if (sym == '\0')
		return (&ptr[i]);
	return (NULL);
}
