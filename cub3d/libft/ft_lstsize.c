#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		sum;
	t_list	*list;

	list = lst;
	sum = 0;
	if (lst != NULL)
	{
		while (list)
		{
			list = list->next;
			sum++;
		}
	}
	return (sum);
}
